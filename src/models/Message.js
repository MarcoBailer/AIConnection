import mongoose from 'mongoose';

const MessageSchema = new mongoose.Schema({
  author: {
    type: String,
    enum: ['user', 'ai'],
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  timestamp: {
    type: Date,
    default: Date.now,
  },
});

export default MessageSchema;
